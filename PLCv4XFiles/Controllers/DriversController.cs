using PLCv4XFiles.Models;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Net;
using System.Net.Http;
using System.Web.Http;

namespace PLCv4XFiles.Controllers
{
    public class DriversController : ApiController

    {


       Driver[] Drivers = new Driver[]
       {
            new Driver { Id = 1, tagValor = "Tag.C0", tagSerie = "Tag.C1"},
            new Driver { Id = 2, tagValor = "Tag.C0", tagSerie = "Tag.C1"},
            new Driver { Id = 3, tagValor = "Tag.C0", tagSerie = "Tag.C1"},
       };

        public IEnumerable<Driver> GetAllDrivers()
        {
            return Drivers;
        }


        public IHttpActionResult GetDriver(int id)
        {
            var DriverAux = Drivers.FirstOrDefault((p) => p.Id == id);

            if (DriverAux == null)

            {

                return NotFound();

            }


            return Ok(DriverAux);
        }






    }
}
